"""print the first 10 natural numbers using for loop"""
a = 10
for i in range(0, a):  # for loop start with zero index.
    print(i)

"""Python program to print all the even numbers within the given range is 10."""

a = 10
for i in range(0, a):
    if i % 2 == 0:  # even numbers or divisible by 2
        print(i)